package Questão5;
// Herança é um pilar da orientaçãoa objetos onde, uma classe principal(mãe), define métodos, ações e atributos
// Seus filhos podem instaciar os metodos sem necessidade de criar novos métodos;

public class Classes {

    protected int a = 2;
    public int getA() {
        return a;
    }
    public void setA(int a) {
        this.a = a;
    }
    protected int b = 2;
    public int getB() {
        return b;
    }
    public void setB(int b) {
        this.b = b;
    }
        
}